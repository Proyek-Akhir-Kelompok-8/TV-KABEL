    private void setTableContent() throws SQLException{
        DefaultTableModel model = (DefaultTableModel) tabelPelanggan.getModel();
        String query = String.format("SELECT * FROM pelanggan");
        model.setRowCount(0);
        ResultSet values = db.select(query);
        while (values.next()) {            
            String idPelanggan = values.getString(1);
            String nama = values.getString(2);
            String idPaket = values.getString(3);
            String alamat = values.getString(4);
            String noHP = values.getString(5);
            String email = values.getString(6);
            String tanggalBerlangganan = values.getString(7);
            String statusLangganan = values.getString(8);
            String idPegawai = values.getString(9);
            String[] data = {idPelanggan,nama,idPaket,alamat,noHP,email,tanggalBerlangganan,statusLangganan,idPegawai};
            model.addRow(data);
        }
        
    }
    
    private void tabelPelangganMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelPelangganMouseClicked
        try {
            txtIdPel.setEnabled(false);
            int row = tabelPelanggan.getSelectedRow();
            DefaultTableModel model = (DefaultTableModel) tabelPelanggan.getModel();
            Object selectedRow = tabelPelanggan.getValueAt(row, 0);
            String query = String.format("SELECT * FROM pelanggan WHERE ID_Pelanggan = '%s'", selectedRow);
            ResultSet values = db.select(query);
            while (values.next()) {
                txtIdPel.setText(values.getString(1));
                txtNama.setText(values.getString(2));
                txtAlm.setText(values.getString(4));
                txtNo.setText(values.getString(5));
                txtEm.setText(values.getString(6));
                txtTang.setText(values.getString(7));
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(menupelanggan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tabelPelangganMouseClicked

