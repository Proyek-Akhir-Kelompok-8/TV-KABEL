    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    try {
        String idPelanggan = txtIdPel.getText();
        String nama = txtNama.getText();
        String idPaket = (String) pilIdPak.getSelectedItem();
        String alamat = txtAlm.getText();
        String noHP = txtNo.getText();
        String email = txtEm.getText();
        String tanggalBerlangganan = txtTang.getText();
        String statusLangganan = (String) pilStat.getSelectedItem();
        String idPegawai = (String) pilIdPeg.getSelectedItem();

        Pelanggan pelanggan = new Pelanggan();
        boolean result = pelanggan.updatePelanggan(idPelanggan, nama, idPaket, alamat, noHP, email, tanggalBerlangganan, statusLangganan, idPegawai);

        if (result) {
            setTableContent();
            clearField();
            JOptionPane.showMessageDialog(null, "Data pelanggan berhasil diperbarui");
        } else {
            JOptionPane.showMessageDialog(null, "Gagal memperbarui data pelanggan atau data tidak ditemukan.");
        }
    } catch (SQLException ex) {
        Logger.getLogger(menupelanggan.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_jButton2ActionPerformed

